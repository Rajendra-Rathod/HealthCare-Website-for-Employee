
package bean;
public class ChemistBean 
{
    private String name;
   private String area;
   
   public ChemistBean()
   {
       
   }

    public void setName(String name) {
        this.name=name;
    }

    public void setArea(String area) {
        this.area = area;
    }

   

    public String getName() {
       
        return name;
    }

    public String getArea() {
        return area;
    }
   
   
    
}
